const isHslColor = require(".");

describe("ishslColor", () => {
    it.each([[1], [[]], [{}], [null], [undefined]])("should refuse non string values", (value) => {
        expect(isHslColor(value)).toBe(false);
    });

    it.each([
        ["hsl (255,255,255)"],
        ["hsla (255,255,255)"],

        ["hsl( 0,255,255)"],
        ["hsla( 0,255,255)"],

        ["hsl(-1,255,255)"],
        ["hsla(-1,255,255)"],

        ["hsl(361,0,0)"],
        ["hsla(361,0,0)"],

        ["hsl(255,101 ,255)"],
        ["hsla(255,101 ,255)"],

        ["hsl(255,-1,255)"],
        ["hsla(255,-1,255)"],

        ["hsl(255,257,255)"],
        ["hsla(255,257,255)"],

        ["hsl(255,255,255 )"],
        ["hsla(255,255,255 )"],

        ["hsl(255,255,-1)"],
        ["hsla(255,255,-1)"],

        ["hsl(255,255,257)"],
        ["hsla(255,255,257)"],

        ["hsla(255,255,257,1 )"],
        ["hsla(255,255,257,1.1)"],
        ["hsla(255,255,257,-0.1)"],
    ])("should refuse bad forms '%s'", (value) => {
        expect(isHslColor(value)).toBe(false);
    });

    it.each([
        ["hsl(0,0,0)"],
        ["hsl(0, 0,0)"],
        ["hsl(0, 0, 0)"],
        ["hsl(0,0, 0)"],

        ["hsl(1,1,1)"],
        ["hsl(1, 1,1)"],
        ["hsl(1, 1, 1)"],
        ["hsl(1,1, 1)"],

        ["hsl(10,10,10)"],
        ["hsl(10, 10,10)"],
        ["hsl(10, 10, 10)"],
        ["hsl(10,10, 10)"],

        ["hsl(100,100,100)"],
        ["hsl(100, 100,100)"],
        ["hsl(100, 100, 100)"],
        ["hsl(100,100, 100)"],

        ["hsl(256,50,33)"],
        ["hsl(256, 50,44)"],
        ["hsl(256, 50, 99)"],
        ["hsl(256,50, 0)"],

        ["hsla(0,0,0)"],
        ["hsla(0, 0,0)"],
        ["hsla(0, 0, 0)"],
        ["hsla(0,0, 0)"],

        ["hsla(1,1,1)"],
        ["hsla(1, 1,1)"],
        ["hsla(1, 1, 1)"],
        ["hsla(1,1, 1)"],

        ["hsla(10,10,10)"],
        ["hsla(10, 10,10)"],
        ["hsla(10, 10, 10)"],
        ["hsla(10,10, 10)"],

        ["hsla(100,100,100)"],
        ["hsla(100, 100,100)"],
        ["hsla(100, 100, 100)"],
        ["hsla(100,100, 100)"],

        ["hsla(360,75,50)"],
        ["hsla(256, 75,50)"],
        ["hsla(256, 30, 50)"],
        ["hsla(360,100, 0)"],

        ["hsla(0,10,55,1)"],
        ["hsla(360,25,25,0)"],
        ["hsla(360,25,25, 1)"],
        ["hsla(360,25,25, 0)"],
        ["hsla(360,25,25,0.1)"],
        ["hsla(360,25,25,0.105975)"],
        ["hsla(360,25,25, 0.254725)"],
    ])("should accept good forms '%s'", (value) => {
        expect(isHslColor(value)).toBe(true);
    });
});
