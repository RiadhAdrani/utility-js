const isRgbColor = require(".");

describe("isRgbColor", () => {
    it.each([[1], [[]], [{}], [null], [undefined]])("should refuse non string values", (value) => {
        expect(isRgbColor(value)).toBe(false);
    });

    it.each([
        ["rgb (255,255,255)"],
        ["rgba (255,255,255)"],

        ["rgb( 0,255,255)"],
        ["rgba( 0,255,255)"],

        ["rgb(-1,255,255)"],
        ["rgba(-1,255,255)"],

        ["rgb(257,255,255)"],
        ["rgba(257,255,255)"],

        ["rgb(255,255 ,255)"],
        ["rgba(255,255 ,255)"],

        ["rgb(255,-1,255)"],
        ["rgba(255,-1,255)"],

        ["rgb(255,257,255)"],
        ["rgba(255,257,255)"],

        ["rgb(255,255,255 )"],
        ["rgba(255,255,255 )"],

        ["rgb(255,255,-1)"],
        ["rgba(255,255,-1)"],

        ["rgb(255,255,257)"],
        ["rgba(255,255,257)"],

        ["rgba(255,255,257,1 )"],
        ["rgba(255,255,257,1.1)"],
        ["rgba(255,255,257,-0.1)"],
    ])("should refuse bad forms '%s'", (value) => {
        expect(isRgbColor(value)).toBe(false);
    });

    it.each([
        ["rgb(0,0,0)"],
        ["rgb(0, 0,0)"],
        ["rgb(0, 0, 0)"],
        ["rgb(0,0, 0)"],

        ["rgb(1,1,1)"],
        ["rgb(1, 1,1)"],
        ["rgb(1, 1, 1)"],
        ["rgb(1,1, 1)"],

        ["rgb(10,10,10)"],
        ["rgb(10, 10,10)"],
        ["rgb(10, 10, 10)"],
        ["rgb(10,10, 10)"],

        ["rgb(100,100,100)"],
        ["rgb(100, 100,100)"],
        ["rgb(100, 100, 100)"],
        ["rgb(100,100, 100)"],

        ["rgb(256,256,256)"],
        ["rgb(256, 256,256)"],
        ["rgb(256, 256, 256)"],
        ["rgb(256,256, 256)"],

        ["rgba(0,0,0)"],
        ["rgba(0, 0,0)"],
        ["rgba(0, 0, 0)"],
        ["rgba(0,0, 0)"],

        ["rgba(1,1,1)"],
        ["rgba(1, 1,1)"],
        ["rgba(1, 1, 1)"],
        ["rgba(1,1, 1)"],

        ["rgba(10,10,10)"],
        ["rgba(10, 10,10)"],
        ["rgba(10, 10, 10)"],
        ["rgba(10,10, 10)"],

        ["rgba(100,100,100)"],
        ["rgba(100, 100,100)"],
        ["rgba(100, 100, 100)"],
        ["rgba(100,100, 100)"],

        ["rgba(256,256,256)"],
        ["rgba(256, 256,256)"],
        ["rgba(256, 256, 256)"],
        ["rgba(256,256, 256)"],

        ["rgba(256,256,256,1)"],
        ["rgba(256,256,256,0)"],
        ["rgba(256,256,256, 1)"],
        ["rgba(256,256,256, 0)"],
        ["rgba(256,256,256,0.1)"],
        ["rgba(256,256,256,0.105975)"],
        ["rgba(256,256,256, 0.254725)"],
    ])("should accept good forms '%s'", (value) => {
        expect(isRgbColor(value)).toBe(true);
    });
});
